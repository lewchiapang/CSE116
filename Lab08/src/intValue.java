
public class intValue {
	
	public int multiply(int number1, int number2){
		int n = number1 * number2;
		return n;
	}
	
	public int add(int number1, int number2){
		int n = number1 + number2;
		return n;
	}
	
	public int pow(long number1, long number2){
		int n = (int) Math.pow(number1, number2);
		return n;
	}
}
